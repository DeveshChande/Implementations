// The Fisher-Yates shuffle is an algorithm for generating a random permutation of a finite sequence, efficiently in-place.

#include <stdlib.h>

void shuffle(int *array, size_t n) {
    if (n > 1) {
        for (size_t i = 0; i < n - 1; i++) {
            size_t j = i + rand() / (RAND_MAX / (n - i) + 1);
            int temp = array[j];
            array[j] = array[i];
            array[i] = temp;
        }
    }
}
